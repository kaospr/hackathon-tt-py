def _indent_body(body: str, indent: int = 8) -> str:
    """Indent every line of a method body by *indent* spaces.

    Strips the body first, then re-indents each line.  Blank lines are
    kept blank (no trailing whitespace).
    """
    prefix = " " * indent
    lines = textwrap.dedent(body).strip().splitlines()
    result = []
    for line in lines:
        if line.strip():
            result.append(prefix + line)
        else:
            result.append("")
    return "\n".join(result)


