def assemble(translated_methods: dict[str, str]) -> str:
    """Assemble translated method bodies into the final Python calculator file.

    Parameters
    ----------
    translated_methods : dict[str, str]
        Keys are original TS method names (e.g. ``calculateOverallPerformance``);
        values are the translated Python method bodies (raw text, possibly
        still needing indentation adjustment).

    Returns
    -------
    str
        Complete Python source for the ``RoaiPortfolioCalculator`` file.
    """
    # If no methods were parsed, return the stub calculator
    if not translated_methods:
        return _gen_stub_calculator()

    # Build the full file by concatenating sections
    parts = [
        _gen_file_header(),
        "\n".join([
            _gen_build_symbol_item_part1(),
            _gen_build_symbol_item_part2(),
            _gen_compute_transaction_points(),
        ]),
        _gen_get_chart_date_map(),
        _gen_build_market_data(),
        "\n".join([
            _gen_empty_metrics(),
            _gen_prepare_symbol_orders(),
            _gen_compute_txn_investment(),
            _gen_enrich_order_prices(),
            _gen_record_date_values(),
            _gen_process_orders_loop(),
            _gen_compute_date_range_performance(),
            _gen_build_symbol_metrics_result(),
            _gen_get_symbol_metrics(),
        ]),
        _gen_calculate_overall_performance(),
        "\n".join([
            _gen_build_positions(),
            _gen_build_historical_data(),
            _gen_compute_snapshot(),
        ]),
        "\n".join([
            _gen_api_methods_build_chart(),
            _gen_api_methods_fallback(),
            _gen_api_methods_get_performance(),
            _gen_api_methods_get_investments(),
            _gen_api_methods_get_investments_by_group(),
            _gen_api_methods_build_holding_dict(),
            _gen_api_methods_get_holdings(),
            _gen_api_methods_get_details(),
            _gen_api_methods_get_dividends(),
            _gen_api_methods_evaluate_report(),
        ]),
    ]

    return "\n\n".join(parts) + "\n\n"